export let CURRENT_LANG = 'en';

export function setLanguage(lang) {
    if (TRANSLATIONS[lang]) {
        CURRENT_LANG = lang;
    } else {
        CURRENT_LANG = 'en';
    }
}

export function detectAndSetLanguage() {
    if (typeof navigator !== 'undefined') {
        const browserLang = navigator.language || navigator.userLanguage;
        if (browserLang && browserLang.toLowerCase().startsWith('pt')) {
            setLanguage('ptBr');
            return;
        }
    }
    setLanguage('en');
}

// Auto detect on load
detectAndSetLanguage();

export function getTranslation(key, params = {}, lang = CURRENT_LANG) {
    let text = TRANSLATIONS[lang]?.[key] || TRANSLATIONS['en']?.[key] || key;
    Object.keys(params).forEach(param => {
        text = text.replace(`{${param}}`, params[param]);
    });
    return text;
}

export function localizeDOM(lang = CURRENT_LANG) {
    if (typeof document !== 'undefined') {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            const translation = getTranslation(key, {}, lang);
            if (translation !== key) {
                if (el.tagName === 'INPUT' && el.type === 'button') {
                    el.value = translation;
                } else {
                    el.textContent = translation;
                }
            }
        });
    }
}